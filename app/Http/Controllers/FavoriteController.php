    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $favorite = Favorite::where([
            ['user_id', '=', Auth::id()],
            ['game_id', '=', $id]
        ])->first();


        $favorite->delete();
        return redirect()->back()->with('info', 'This game is not favorite anymore');
    }
